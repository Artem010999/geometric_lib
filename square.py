def area(a):
    '''Принимает на вход сторону квадрата, возвращает площадь данного квадрата.'''
    return a * a


def perimeter(a):
    '''Принимает на вход сторону квадрата, возвращает периметр данного квадрата.'''
    return 4 * a


print(area(11))
'''Вывыедет 121, т.к. 11 * 11 = 121.'''

print(perimeter(4))
'''Выведет 16, т.к. 4 * 4 = 16.'''
